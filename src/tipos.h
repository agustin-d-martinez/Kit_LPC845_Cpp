/**Defines 'read only' permission or 'Input' */
#define  	__R    					volatile const
/** Defines 'write only' permission or 'Output' */
#define  	__W    					volatile
/** Defines 'read / write' permission or 'Input / Output' */
#define  	__RW   					volatile

typedef		unsigned char 			uint8_t;	/**< Definicion del uint8_t	*/
typedef		char					int8_t;		/**< Definicion del int8_t	*/
typedef		short unsigned int 		uint16_t;	/**< Definicion del uint16_t*/
typedef		short signed int		int16_t;	/**< Definicion del int16_t	*/
typedef		unsigned int 			uint32_t;	/**< Definicion del uint32_t*/
typedef		signed int				int32_t;	/**< Definicion del int32_t	*/

/** Valor máximo del INT32	*/
#define		INT32_MAX				(2147483647)
/** Valor mínimo del INT32	*/
#define		INT32_MIN				(-2147483648)
/** Valor máximo del UINT32*/
#define		UINT32_MAX				(4294967295)

/** Valor máximo del INT8	*/
#define		INT8_MAX				(127)
/** Valor mínimo del INT8	*/
#define		INT8_MIN				(-128)
/** Valor máximo del UINT8	*/
#define		UINT8_MAX				(255)

