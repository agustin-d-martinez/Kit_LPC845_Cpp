/**
 * \class Pin
 * \brief Clase del objeto Pin
 *
 * El objeto Pin debe ser heredado y posee el puerto y bit del periférico en cuestion
*/
class Pin
{
	public:
		/** Enumeracion con los puertos  */
		typedef enum port_t			{ port0 , port1 } port_t;
		enum max_bits_port_t{ b_port0 = 31 , b_port1 = 9 };
		/** Enumeracion de error  */
		typedef enum error_t		{ error = 2 , ok } error_t;

	public:
		const 	port_t m_port ;		/**< Guarda el puerto del pin  */
		const 	uint8_t m_bit ;		/**< Guarda el bit del pin  */
		int8_t  m_error ;			/**< Error en la clase pin  */

	public:
		Pin( port_t port , uint8_t bit );
		virtual ~Pin() {};			/**< Destructor por defecto */
};
