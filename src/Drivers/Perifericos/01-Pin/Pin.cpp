/**
 * \fn Pin::Pin ( port_t port , uint8_t bit )
 * \brief Constructor de clase PIN
 * \details Crea un PIN con los parámetros correspondientes
 * \param [in] port: Puerto del objeto
 * \param [in] bit: Bit del objeto
*/
Pin::Pin ( port_t port , uint8_t bit ) : m_port( port ) , m_bit ( bit )
{
	m_error = ok;
	if ( m_port > port1 )
		m_error = error;
	if ( m_port == port0 && m_bit > b_port0 )
		m_error = error;
	if ( m_port == port1 && m_bit > b_port1 )
		m_error = error;
}
