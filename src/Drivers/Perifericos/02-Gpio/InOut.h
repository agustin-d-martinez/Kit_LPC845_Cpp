/**
 * \class InOut
 * \brief Clase del objeto InOut
 *
 * Clase abstracta pura para la generaci√≥n de GPIO
*/
class InOut
{
	public:
						InOut () = default;				/**< Constructor por defecto */
		virtual uint8_t SetPin ( void ) 		= 0;	/**< Enciende el pin */
		virtual uint8_t ClrPin ( void ) 		= 0; 	/**< Apaga el pin */
		virtual uint8_t SetTogglePin ( void ) 	= 0;  	/**< Cambia el valor del pin */
		virtual uint8_t SetDir ( void )  		= 0;	/**< Setea la direccion del pin */
		virtual uint8_t SetToggleDir ( void )   = 0;	/**< Cambia la direccion del pin */
		virtual uint8_t GetPin ( void )   		= 0;	/**< Obtiene valor del pin */
		virtual uint8_t SetPinMode ( void )  	= 0;	/**< Setea modo del pin */
		virtual uint8_t SetPinResistor ( void )	= 0;	/**< Setea resistencia del pin */
		virtual 		~InOut () = default;			/**< Destructor por defecto */
};
